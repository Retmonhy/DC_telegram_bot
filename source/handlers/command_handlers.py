@bot.message_handler(commands=['start'])
def start_message(message: types.Message):
  markup = types.InlineKeyboardMarkup()
  start_button = types.InlineKeyboardButton("üöÄ –ù–∞—á–∞—Ç—å —Ä–∞—Å—á–µ—Ç", callback_data = 'start_calculating')
  markup.add(start_button)
  model = model_singleton.get_model_by_id(message.chat.id)
  model.go_to_step(CalculatorSteps.start)
  message_text = f'''–ü—Ä–∏–≤–µ—Ç, {message.from_user.first_name} üëã\n–ù—É —á—Ç–æ, –Ω–∞—á–Ω–µ–º —Ä–∞—Å—á–µ—Ç?'''
  bot.send_message(message.chat.id, message_text, reply_markup=markup)

